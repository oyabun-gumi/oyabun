index('GET', [], _) ->
    {ok, {redirect, "/error/err404"}}.

err404('GET', [], _) ->
    {ok, [{json, {"This page is not found."}}]}.

err500('GET', [], _) ->
    {ok, [{json, {"This page has error."}}]}.

